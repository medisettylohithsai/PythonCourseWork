
# Python Operators programs


# ----- Arithmetic Operators -----
a = 10
b = 3
print("Arithmetic Operators:")
print("Addition:", a + b)        # +
print("Subtraction:", a - b)     # -
print("Multiplication:", a * b)  # *
print("Division:", a / b)        # /
print("Floor Division:", a // b) # //
print("Modulus:", a % b)         # %
print("Power:", a ** b)          # **
print()

# ----- Assignment Operators -----
x = 5
print("Assignment Operators:")
print("Initial x:", x)
x += 3   # +=
print("x += 3:", x)
x -= 2   # -=
print("x -= 2:", x)
x *= 4   # *=
print("x *= 4:", x)
x /= 2   # /=
print("x /= 2:", x)
print()

# ----- Comparison Operators -----
a = 10
b = 20
print("Comparison Operators:")
print("a == b:", a == b)  # Equal
print("a != b:", a != b)  # Not Equal
print("a > b:", a > b)    # Greater Than
print("a < b:", a < b)    # Less Than
print("a >= b:", a >= b)  # Greater or Equal
print("a <= b:", a <= b)  # Less or Equal
print()

# ----- Logical Operators -----
x = True
y = False
print("Logical Operators:")
print("x and y:", x and y)  # and
print("x or y:", x or y)    # or
print("not x:", not x)      # not
print()

# ----- Membership Operators -----
list1 = [1, 2, 3, 4, 5]
print("Membership Operators:")
print("2 in list1:", 2 in list1)            # in
print("6 in list1:", 6 in list1)            # in
print("3 not in list1:", 3 not in list1)    # not in
print()

# ----- Identity Operators -----
x = 10
y = 10
print("Identity Operators:")
print("x is y:", x is y)            # is
print("x is not y:", x is not y)    # is not
print()

# ----- Bitwise Operators -----
a = 5  # 0101
b = 3  # 0011
print("Bitwise Operators:")
print("a & b:", a & b)    # AND
print("a | b:", a | b)    # OR
print("a ^ b:", a ^ b)    # XOR
print("~a:", ~a)          # NOT
print("a << 1:", a << 1)  # Left Shift
print("a >> 1:", a >> 1)  # Right Shift
